class A6
{
public static void main(String args[])
{
  for(int i=1;i<=5;i++) //col
  {
    for(int j=2;j<=i;j++) //spaces
	{
	System.out.print(" ");
	} 
	
    for(int j=5;j>=i;j--) //star
	{
	System.out.print("*");
	}
	
	for(int j=4;j>=i;j--) //star
	{
	System.out.print("*");
	}
	
    System.out.println();
  } 

} 


}

